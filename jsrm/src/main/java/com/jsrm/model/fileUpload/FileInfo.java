package com.jsrm.model.fileUpload;

import com.jsrm.base.common.BaseModel;

import java.util.Date;

/**
 * 附件上传实体类
 * Created by wdCao on 2016/10/19.
 */
public class FileInfo extends BaseModel {
    private String id;
    private String fileName;                //文件名称
    private String fileFormat;              //文件格式
    private String filePath;                //文件路径
    private String fileReName;              //文件别名
    private String fileSize;                //文件大小
    private String MD5Code;                 //MD5code
    private String relateType;              //关联业务类型
    private String relateId;                //关联业务id
    private String uploadUserId;            //上传用户id
    private String uploadUserName;          //上传用户姓名
    private String state;                   //状态：1：上传中 0：删除 2上传完成
    private Date uploadTime;                //上传时间

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getFileFormat() {
        return fileFormat;
    }

    public void setFileFormat(String fileFormat) {
        this.fileFormat = fileFormat;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getFileReName() {
        return fileReName;
    }

    public void setFileReName(String fileReName) {
        this.fileReName = fileReName;
    }

    public String getFileSize() {
        return fileSize;
    }

    public void setFileSize(String fileSize) {
        this.fileSize = fileSize;
    }

    public String getRelateType() {
        return relateType;
    }

    public void setRelateType(String relateType) {
        this.relateType = relateType;
    }

    public String getRelateId() {
        return relateId;
    }

    public void setRelateId(String relateId) {
        this.relateId = relateId;
    }

    public String getUploadUserId() {
        return uploadUserId;
    }

    public void setUploadUserId(String uploadUserId) {
        this.uploadUserId = uploadUserId;
    }

    public String getUploadUserName() {
        return uploadUserName;
    }

    public void setUploadUserName(String uploadUserName) {
        this.uploadUserName = uploadUserName;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public Date getUploadTime() {
        return uploadTime;
    }

    public void setUploadTime(Date uploadTime) {
        this.uploadTime = uploadTime;
    }

    public String getMD5Code() {
        return MD5Code;
    }

    public void setMD5Code(String MD5Code) {
        this.MD5Code = MD5Code;
    }
}
