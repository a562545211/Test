package com.jsrm.web.vo.approve;

import com.jsrm.model.approve.ApproveInstance;

/**
 * 流程实例vo
 * Created by wdCao on 2016/11/2.
 */
public class ApproveInstanceVo extends ApproveInstance {

    private String approveOpinion;      //审批意见
    private String isPass;              //是否同意

    private String toUserId;            //转发给用户id
    private String toUserName;          //转发给用户姓名

    private String loginUserId;         //当前登录人id

    private String goBackIds;           //批量退回ids

    public String getApproveOpinion() {
        return approveOpinion;
    }

    public void setApproveOpinion(String approveOpinion) {
        this.approveOpinion = approveOpinion;
    }

    public String getIsPass() {
        return isPass;
    }

    public void setIsPass(String isPass) {
        this.isPass = isPass;
    }

    public String getToUserId() {
        return toUserId;
    }

    public void setToUserId(String toUserId) {
        this.toUserId = toUserId;
    }

    public String getToUserName() {
        return toUserName;
    }

    public void setToUserName(String toUserName) {
        this.toUserName = toUserName;
    }

    public String getGoBackIds() {
        return goBackIds;
    }

    public void setGoBackIds(String goBackIds) {
        this.goBackIds = goBackIds;
    }

    public String getLoginUserId() {
        return loginUserId;
    }

    public void setLoginUserId(String loginUserId) {
        this.loginUserId = loginUserId;
    }
}
